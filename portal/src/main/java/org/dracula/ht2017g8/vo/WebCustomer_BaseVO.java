package org.dracula.ht2017g8.vo;

public class WebCustomer_BaseVO {

    private String custnm;

    private String title;

    private String age;

    private String wkname;

    private String incom;

    private String childrens;

    private String cars;

    private String house;

    public String getCustnm() {
        return custnm;
    }

    public void setCustnm(String custnm) {
        this.custnm = custnm;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getWkname() {
        return wkname;
    }

    public void setWkname(String wkname) {
        this.wkname = wkname;
    }

    public String getIncom() {
        return incom;
    }

    public void setIncom(String incom) {
        this.incom = incom;
    }

    public String getChildrens() {
        return childrens;
    }

    public void setChildrens(String childrens) {
        this.childrens = childrens;
    }

    public String getCars() {
        return cars;
    }

    public void setCars(String cars) {
        this.cars = cars;
    }

    public String getHouse() {
        return house;
    }

    public void setHouse(String house) {
        this.house = house;
    }
}
